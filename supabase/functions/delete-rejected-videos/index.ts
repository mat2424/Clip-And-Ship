import { serve } from "https://deno.land/std@0.190.0/http/server.ts";
import { createClient } from "https://esm.sh/@supabase/supabase-js@2";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    console.log('üóëÔ∏è Starting deletion of rejected video ideas');

    const supabaseUrl = Deno.env.get('SUPABASE_URL')!;
    const supabaseKey = Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')!;

    // Create Supabase client with service role key for admin access
    const supabase = createClient(supabaseUrl, supabaseKey);

    // Delete all rejected video ideas
    const { data, error } = await supabase
      .from('video_ideas')
      .delete()
      .eq('approval_status', 'rejected');

    if (error) {
      console.error('‚ùå Error deleting rejected videos:', error);
      return new Response(
        JSON.stringify({ error: 'Failed to delete rejected videos', details: error.message }),
        { 
          status: 500, 
          headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
        }
      );
    }

    console.log('‚úÖ Successfully deleted rejected videos:', data);

    return new Response(
      JSON.stringify({ 
        success: true, 
        message: 'Rejected videos deleted successfully',
        deletedCount: data ? data.length : 0
      }),
      { 
        status: 200, 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
      }
    );

  } catch (error) {
    console.error('üí• Unexpected error:', error);
    return new Response(
      JSON.stringify({ 
        error: 'Internal server error', 
        details: error.message 
      }),
      { 
        status: 500, 
        headers: { ...corsHeaders, 'Content-Type': 'application/json' } 
      }
    );
  }
});